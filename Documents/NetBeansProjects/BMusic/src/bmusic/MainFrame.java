    private void btnPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlayActionPerformed
        // TODO add your handling code here:
        
        if(T_MAIN != null && T_MAIN.isAlive()){
            audioPlayer.stop();
            T_MAIN.interrupt();
            while (true) {
               if(!T_MAIN.isAlive())
                   break;
                try {
                    Thread.sleep(50);
                } catch (InterruptedException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            ImageIcon image = new ImageIcon(Paths.get(".").toAbsolutePath().normalize().toString()+"\\src\\img\\play_32px.png");
            btnPlay.setIcon(image );
        }else{
            ImageIcon image = new ImageIcon(Paths.get(".").toAbsolutePath().normalize().toString()+"\\src\\img\\pause.png");
            btnPlay.setIcon(image );
            T_MAIN = new Thread(){
                @Override
                public void run(){
                    audioPlayer.play();
                        
                }
            };
            T_MAIN.start();
        }
       
        
    }//GEN-LAST:event_btnPlayActionPerformed

